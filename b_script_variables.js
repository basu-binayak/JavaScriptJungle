// Values and Variables
/////////////////////////

// variables are used to store values 
let name = 'Soham Gang'; //the value 'Soham Gang' is stored in variable name 

console.log(`The name of the person is ${name}`);
// o/p: The name of the person is Soham Gang

console.log(`The person who was asking you the address goes by the name: ${name}`);
// o/p: The person who was asking you the address goes by the name: Soham Gang

// hence once a value is stored in a variable it can be used any number of times 
